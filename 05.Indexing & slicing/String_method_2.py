s="python"
m1=s.ljust(10,"*")
print(m1)
print("---------------------------------------")#ljust method
m2=s.rjust(10,"*")
print(m2)
print("---------------------------------------")#rjust method
m3=s.center(10,"*")
print(m3)
print("---------------------------------------")# center method
m4=s.zfill(10)
print(m4)
print("---------------------------------------")# zfill method